## ARITHMETIC OPERATORS
print(1 + 1) # Addition
print(3 - 1) # Subtraction
print(2 * 5) # Multiplication
print(6 / 3) # Division
print (2 ** 2) # Exponentiation (Power)
print(6 % 2) # Modulus (Remainder)

## AUGMENTED OPERATORS
num = 2

num += 2 # Augmented Addidion
num -= 1 # Augmented Subtraction
num *= 2 # Augmented Division
num /= 4 # Augmented Division
num **= 3 # Augmented Exponentiation (Power)