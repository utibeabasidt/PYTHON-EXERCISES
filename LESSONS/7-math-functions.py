## MATH IMPORT
import math as m
print('MATH IMPORTS')
print(m.pi) # prints the value of pi
print(m.e) # prints the exponential constant
print(m.sqrt(4)) # prints the squared root of a value
print(m.cbrt(27)) # prints the cube root of a value
print(m.ceil(3.4)) # prints the nearest top most whole number
print(m.floor(3.6)) # prints the nearest down most whole number
print(m.trunc(3.9)) # truncate a value
print(m.cos(30)) # print the cosine of a number
print(m.sin(30)) # print the sine of a number
print(m.tan(30)) # print the tangent of a number
print(m.degrees(1)) # converts from radians to degrees
print(m.radians(57.29577951308232)) # convers from degrees to radians