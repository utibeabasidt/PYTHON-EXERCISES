# IF STATEMENT
if 3 > 2:
  print('3 is greater than 2')


## FOR ALL OTHER STATMENTS: If a condition is true and it works, the other conditions won't run
# IF ... ELSE STATEMENT
if 2 > 3:
  print('3 is greater than 3')
else:
  print('2 is not greater than 3')

# IF ... ELIF STATMENT
if type(3) == int:
  print('3 is an integer')
elif type('utibe') == str:
  print('utibe is a string')

