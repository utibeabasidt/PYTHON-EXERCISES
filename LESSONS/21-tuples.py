my_tuple = (1, 2, 3, 4, 5)

print(my_tuple) # this prints the whole tuple
print(len(my_tuple)) # this returns the total number of values inside the tuple
print(5 in my_tuple) # this checks if a value is in a tuple
print(3 not in my_tuple) # this checks if a value is not in a tuple
print(my_tuple.index(5)) # this gives the index of a value
print(my_tuple.count(5)) # this counts the number of times a value has occured itself in the tuple
del my_tuple # this permanently deletes the tuple