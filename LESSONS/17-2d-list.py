my_list = [
  [1, 2, 3],
  [4, 5, 6],
  [7, 8, 9],
  ['*', 0, '#']
]

print(my_list)

## INDEXING(single indexing)
print(my_list[0]) # gives you the first row of list (1, 2, 3)
print(my_list[2]) # gives you the third row of list (7, 8, 9)

## INDEXING(double indexing)
print(my_list[0][0]) # gives you the intersection of the fist row and the first column (1)
print(my_list[1][2]) # gives you the intersection of the second row and the last column (6)